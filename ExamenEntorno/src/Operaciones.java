
public class Operaciones {
	
	public String Union(String a, String b)
	{
		return a+b;
	}
	
	public String May(String a)
	{
		return a.toUpperCase();
	}
	
	public int Long(String a)
	{
		return a.length();
	}
	
	public boolean Comp(int a, int b)
	{
		return a>=b;
	}
	
	public int Cuadrado(int a)
	{
		return a*a;
	}
	
	public boolean Diez(int a)
	{
		return a>=10;
	}
}
